# Operadores de comparação entre dados no Python

# == - Operador de Igualdade 
# != - Operador de Diferença
# >  - Operador de Maior que
# <  - Operador de Menor que
# >= - Operador de Maior ou Igual
# <= - Operador de Menor ou Igual

# Igualdade
# print(2 == 3)

# Diferenca
# print(2 != 3)

print(2 == 2)
print(5 > 4)
print(5 < 4)
print(5 >= 5)
print(5 <= 6)
